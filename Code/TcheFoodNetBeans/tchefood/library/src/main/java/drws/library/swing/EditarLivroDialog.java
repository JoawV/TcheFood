    private boolean isEverythingOk() {
        boolean isOk = true;
        
        if(comboBoxAutor.getSelectedIndex() == 0) {
            comboBoxAutor.setBackground(Color.red);
            comboBoxAutor.requestFocus();
            isOk = false;
        }
        else  {
            comboBoxAutor.setBackground(Options.textFieldcolor);
        }
        if(textFieldAno.getText().isBlank()) {
            textFieldAno.setBackground(Color.red);
            textFieldAno.requestFocus();
            isOk = false;
        }
        else {
            textFieldAno.setBackground(Options.textFieldcolor);
        }
        if(textFieldPaginas.getText().isBlank()) {
            textFieldPaginas.setBackground(Color.red);
            textFieldPaginas.requestFocus();
            isOk = false;
        }
        else {
            textFieldPaginas.setBackground(Options.textFieldcolor);
        }
        if(textFieldGenero.getText().isBlank()) {
            textFieldGenero.setBackground(Color.red);
            textFieldGenero.requestFocus();
            isOk = false;
        }
        else {
            textFieldGenero.setBackground(Options.textFieldcolor);
        }
        if(textFieldTitulo.getText().isBlank()) {
            textFieldTitulo.setBackground(Color.red);
            textFieldTitulo.requestFocus();
            isOk = false;
        }
        else {
            textFieldTitulo.setBackground(Options.textFieldcolor);
        }
        return isOk;
    }
    
    private void loadDados(int indexLivro) {
        Livro livro = this.bancoDados.getBiblioteca().getLivros().get(indexLivro);
        
        textFieldCodigo.setText(String.valueOf(livro.getId()));
        textFieldTitulo.setText(livro.getTitle());
        textFieldGenero.setText(livro.getGenre());
        textFieldAno.setText(livro.getYear());
        textFieldPaginas.setText(livro.getPages());
        comboBoxAutor.setSelectedIndex(this.bancoDados.getIndexAutor(livro.getAuthor()) + 1);
    }
    
    private void loadAutores() {
        List<Autor> autores = this.bancoDados.getAutores();
        
        for(int i = 0; i < autores.size(); i++) {
            String codigo = String.valueOf(autores.get(i).getId());
            String nomeAutor = autores.get(i).getName();
            String item = codigo + " - " + nomeAutor;
            this.comboBoxAutor.addItem(item);
        }
    }
