    private void disableButtonSelectedTheme() {
        int theme = Options.selectedTheme;
        
        switch (theme) {
            case 0:
                this.buttonFlatDarkPurple.setEnabled(false);
                break;
            case 1:
                this.buttonFlatHighContrast.setEnabled(false);
                break;
            case 2:
                this.buttonFlatSolarizedLight.setEnabled(false);
                break;
            case 3:
                this.buttonFlatSolarizedDark.setEnabled(false);
                break;
            case 4:
                this.buttonFlatMonocai.setEnabled(false);
                break;
            case 5:
                this.buttonFlatCarbon.setEnabled(false);
                break;
            case 6:
                this.buttonFlatLightFlat.setEnabled(false);
                break;
            case 7:
                this.buttonFlatLightGray.setEnabled(false);
                break;
            default:
                throw new AssertionError();
        }
            
    }
    
