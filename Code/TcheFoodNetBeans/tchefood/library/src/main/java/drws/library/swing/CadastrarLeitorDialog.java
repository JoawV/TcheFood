    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        if(this.isEverythingOk()) {
            int codigo = Integer.parseInt(this.textFieldCodigo.getText());
            String cpf = this.textFieldCPF.getText();
            String nome = this.textFieldNome.getText();
            String telefone  = this.textFieldTelefone.getText();
            String email = this.textFieldEmail.getText();
            String endereco = this.textFieldEndereco.getText();
            String cidade = this.textFieldCidade.getText();
            String estado = this.comboBoxEstado.getSelectedItem().toString();

            Leitor leitor = new Leitor(codigo, cpf, nome, telefone, email, endereco, cidade, estado);
            this.bancoDados.cadastrarLeitor(leitor);
            this.limparTela();
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

