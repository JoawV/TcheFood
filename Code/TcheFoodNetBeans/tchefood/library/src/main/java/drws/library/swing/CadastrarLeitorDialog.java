    private void textFieldCPFFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textFieldCPFFocusLost
        if(textFieldCPF.getText().length() == 11) {
            String cpf = textFieldCPF.getText();
            String cpfMascarado = cpf.substring(0, 3);
            cpfMascarado += ".";
            cpfMascarado += cpf.substring(3, 6);
            cpfMascarado += ".";
            cpfMascarado += cpf.substring(6, 9);
            cpfMascarado += "-";
            cpfMascarado += cpf.substring(9);
            System.out.println(cpfMascarado);
            textFieldCPF.setText(cpfMascarado);    
            textFieldCPF.setBackground(Options.textFieldcolor);  
        }
        else {
            textFieldCPF.setBackground(Color.red);  
        }
    }//GEN-LAST:event_textFieldCPFFocusLost

