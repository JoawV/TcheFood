    private void textFieldCPFKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textFieldCPFKeyTyped
        char c = evt.getKeyChar();
        if(!Character.isDigit(c) || c == KeyEvent.VK_ENTER || c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE || c == KeyEvent.VK_TAB || textFieldCPF.getText().length() == 11 || textFieldCPF.getText().length() == 14) {
                evt.consume();        
        }
    }//GEN-LAST:event_textFieldCPFKeyTyped

