    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        if(isEverythingOk()) {
            int codigoEmprestimo = Integer.parseInt(this.textFieldCodigoEmprestimo.getText());
            String data = this.textFieldData.getText();
            String devolucao = this.textFieldDevolucao.getText();

            Emprestimo emprestimo = new Emprestimo(codigoEmprestimo, data, devolucao, leitor, livro);
            this.bancoDados.getBiblioteca().cadastrarEmprestimo(emprestimo);
            this.bancoDados.getBiblioteca().removerLivro(livro);
            this.limparTela();
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

