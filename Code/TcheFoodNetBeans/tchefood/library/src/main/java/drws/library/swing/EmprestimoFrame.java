    private void limparTela() {
        String codigo = String.valueOf(this.bancoDados.getBiblioteca().getCodigoEmprestimo());
        this.textFieldCodigoEmprestimo.setText(codigo);
        //textFieldData.setText(null);
        textFieldDevolucao.setText(null);
        textFieldCodigoLeitor.setText(null);
        textFieldCPF.setText(null);
        textFieldNome.setText(null);
        textFieldCodigoLivro.setText(null);
        textFieldTitulo.setText(null);
        textFieldAutor.setText(null);
        this.textFieldDevolucao.requestFocus();
    }
    
    private boolean isEverythingOk() {
        boolean isOk = true;
        
        if(textFieldCodigoLeitor.getText().isBlank()) {
            textFieldCodigoLeitor.setBackground(Color.red);
            isOk = false;
        }
        else
        {
            textFieldCodigoLeitor.setBackground(Options.textFieldcolor);
        }
        if(textFieldCodigoLivro.getText().isBlank()) {
            textFieldCodigoLivro.setBackground(Color.red);
            isOk = false;
        }
        else
        {
            textFieldCodigoLivro.setBackground(Options.textFieldcolor);
        }
        if(textFieldDevolucao.getText().isBlank()) {
            textFieldDevolucao.setBackground(Color.red);
            textFieldDevolucao.requestFocus();
            isOk = false;
        }
        else
        {
            textFieldDevolucao.setBackground(Options.textFieldcolor);
        }
        
        return isOk;
    }
    
    private void setDate() {
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date date = new Date();
        
        this.textFieldData.setText(String.valueOf(dateFormat.format(date)));
    }
    
    public void selecionarLivro(Livro livro) {
        this.livro = livro;
        this.textFieldCodigoLivro.setText(String.valueOf(livro.getId()));
        this.textFieldTitulo.setText(livro.getTitle());
        this.textFieldAutor.setText(livro.getAuthor().getName());
        textFieldCodigoLivro.setBackground(Options.textFieldcolor);

        if(!textFieldCodigoLeitor.getText().isBlank() && !textFieldDevolucao.getText().isBlank()) {
            buttonSalvar.requestFocus();
        }        
        if(textFieldCodigoLeitor.getText().isBlank()) {
            textFieldCodigoLeitor.requestFocus();
        }
        if(textFieldDevolucao.getText().isBlank()) {
            textFieldDevolucao.requestFocus();
        }
    }
    
