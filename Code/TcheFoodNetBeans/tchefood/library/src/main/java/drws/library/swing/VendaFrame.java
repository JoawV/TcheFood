    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        if(isEverythingOk()) {
            int codigoVenda = Integer.parseInt(this.textFieldCodigoVenda.getText());
            String data = this.textFieldData.getText();
            //String devolucao = this.textFieldDevolucao.getText();

            Venda venda = new Venda(codigoVenda, data, leitor, livros);
            this.bancoDados.getLoja().cadastrarVenda(venda);
            //this.bancoDados.getLoja().removerLivro(livro);
            this.livros = new ArrayList<>();
            this.limparTela();
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

