    private void textFieldCodigoLeitorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textFieldCodigoLeitorFocusLost
        try {
            int codigo = Integer.parseInt(textFieldCodigoLeitor.getText());
            int index = this.bancoDados.getIndexLeitorPeloCodigo(codigo);
            leitor = this.bancoDados.getLeitores().get(index);
            this.textFieldCPF.setText(leitor.getCpf());
            this.textFieldNome.setText(leitor.getName());
            this.textFieldCodigoLeitor.setBackground(Options.textFieldcolor);
            //this.buttonBuscaLeitor.setFocusable(false);
        }
        catch(Exception e) {
            this.textFieldCodigoLeitor.setText(null);
            this.textFieldCPF.setText(null);
            this.textFieldNome.setText(null);
            this.textFieldCodigoLeitor.setBackground(Color.red);
            this.textFieldCodigoLeitor.requestFocus();
        }
    }//GEN-LAST:event_textFieldCodigoLeitorFocusLost

    private void textFieldCodigoLivroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textFieldCodigoLivroFocusLost
        try {
            int codigo = Integer.parseInt(textFieldCodigoLivro.getText());
            int index = this.bancoDados.getLoja().getIndexLivroPeloCodigo(codigo);
            livro = this.bancoDados.getLoja().getLivros().get(index);
            this.textFieldTitulo.setText(livro.getTitle());
            this.textFieldAutor.setText(livro.getAuthor().getName());
            this.textFieldPreco.setText(String.valueOf(livro.getPrice()));
            this.textFieldCodigoLivro.setBackground(Options.textFieldcolor);
            this.buttonAddLivro.setEnabled(true);
            //this.buttonBuscaLeitor.setFocusable(false);
        }
        catch(Exception e) {
            this.textFieldCodigoLivro.setText(null);
            this.textFieldTitulo.setText(null);
            this.textFieldAutor.setText(null);
            this.textFieldPreco.setText(null);
            this.textFieldCodigoLivro.setBackground(Color.red);
            this.buttonAddLivro.setEnabled(false);
            
            if(this.livros.size() < 1 && !this.textFieldCodigoLeitor.getText().isBlank()) {
                this.textFieldCodigoLivro.requestFocus();
            }
        }
    }//GEN-LAST:event_textFieldCodigoLivroFocusLost

