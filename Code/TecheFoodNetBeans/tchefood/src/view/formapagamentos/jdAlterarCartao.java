    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
            Model.ModelFormaPagamento a = new ModelFormaPagamento();
            int codigo = (int) (JTConsultaPagamento.getValueAt(JTConsultaPagamento.getSelectedRow(), 0));
            
            a.setId(codigo);
            a.setNome(txtNomeAlterar.getText());
            a.setDescricao((String) jCBtipo.getSelectedItem());
            
            DAO.FormaPagamentoDAO.atualizar(a);
            JOptionPane.showMessageDialog(this, "Atualizado com Sucesso!");
            this.dispose();
    }//GEN-LAST:event_jbAlterarActionPerformed

