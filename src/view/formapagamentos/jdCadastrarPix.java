    private boolean btnEnable(String regex, JTextField abc) {
        String tx = abc.getText();
        if (tx.matches(regex)) {
            abc.setForeground(Color.black);
            return true;

        } else {
            abc.setForeground(Color.red);
            abc.requestFocus();
            return false;
        }
    }

